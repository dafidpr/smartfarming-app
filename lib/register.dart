  void _register() async {
    setState(() {
      _isLoading = true;
    });
    var data = {
      'name': name,
      'username': username,
      'password': password,
      'email': email,
      'phone': phone,
      'land_area': landArea,
      'serial_number': serialNumber,
      'farmer_group_id': _farmerGroup
    };

    var res = await Network().authData(data, '/register');
    var body = json.decode(res.body);
    print(body);
    if (body['success']) {
      Navigator.push(
        context,
        new MaterialPageRoute(builder: (context) => Login()),
      );
    }
    setState(() {
      _isLoading = false;
    });
  }
